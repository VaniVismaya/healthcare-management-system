const normalizePath = (p) => (p ? p.replace(/\\/g, '/') : p);
const parseNormalRange = (rangeText) => {
  if (!rangeText) return null;
  const matches = String(rangeText).match(/-?\d+(\.\d+)?/g);
  if (!matches || matches.length < 2) return null;
  const min = Number(matches[0]);
  const max = Number(matches[1]);
  if (!Number.isFinite(min) || !Number.isFinite(max)) return null;
  return { min, max };
};
const computeResultFlag = (valueText, rangeText) => {
  if (valueText === null || valueText === undefined || valueText === '') return null;
  const valueNum = Number(String(valueText).replace(/[^0-9.\-]/g, ''));
  if (!Number.isFinite(valueNum)) return null;
  const range = parseNormalRange(rangeText);
  if (!range) return null;
  if (valueNum < range.min || valueNum > range.max) return 'abnormal';
  return 'normal';
};

const resolveLabOwner = async (userId) => {
  const [[profile]] = await pool.query(
    'SELECT user_id FROM laboratory_profiles WHERE user_id = ?',
    [userId]
  );
  if (profile) return { ownerId: userId, isOwner: true };

  const [[org]] = await pool.query(
    `SELECT r.org_id
     FROM org_user_roles ur
     JOIN org_roles r ON ur.org_role_id = r.id
     WHERE ur.user_id = ? AND r.org_type = 'laboratory'
     LIMIT 1`,
    [userId]
  );
  if (!org) return { ownerId: null, isOwner: false };
  return { ownerId: org.org_id, isOwner: false };
};

