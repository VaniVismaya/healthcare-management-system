// Search labs near location
exports.searchLabs = async (req, res) => {
  const { city, name, latitude, longitude, radius_km } = req.query;
  const lat = Number.parseFloat(latitude);
  const lng = Number.parseFloat(longitude);
  const radius = Number.parseFloat(radius_km) || 10;
  const useGeo = Number.isFinite(lat) && Number.isFinite(lng);
  const distanceExpr = `6371 * acos(
    cos(radians(?)) * cos(radians(lp.latitude)) * cos(radians(lp.longitude) - radians(?)) +
    sin(radians(?)) * sin(radians(lp.latitude))
  )`;
  try {
    let query = `SELECT u.id, u.name, u.profile_image, lp.lab_name, lp.address, lp.city, lp.phone,
      lp.latitude, lp.longitude, lp.working_hours_start, lp.working_hours_end,
      lph.photos
      ${useGeo ? `, (${distanceExpr}) as distance_km` : ''}
      FROM users u JOIN laboratory_profiles lp ON u.id = lp.user_id
      LEFT JOIN (
        SELECT lab_id, GROUP_CONCAT(photo_path) as photos
        FROM lab_photos
        GROUP BY lab_id
      ) lph ON lph.lab_id = u.id
      WHERE u.is_verified = 1 AND u.is_active = 1 AND lp.is_verified = 1`;
    const params = [];
    if (useGeo) {
      query += ' AND lp.latitude IS NOT NULL AND lp.longitude IS NOT NULL';
    }
    if (city) { query += ' AND lp.city LIKE ?'; params.push(`%${city}%`); }
    if (name) { query += ' AND (lp.lab_name LIKE ? OR u.name LIKE ?)'; params.push(`%${name}%`, `%${name}%`); }
    if (useGeo) {
      params.push(lat, lng, lat);
      query += ' HAVING distance_km <= ?';
      params.push(radius);
      query += ' ORDER BY distance_km ASC, lp.lab_name';
    } else {
      query += ' ORDER BY lp.lab_name';
    }
    const [labs] = await pool.query(query, params);
    const mapped = labs.map((l) => ({ ...l, photos: l.photos ? l.photos.split(',') : [] }));
    res.json({ labs: mapped });
  } catch (err) { res.status(500).json({ error: err.message }); }
};

