// Get reports list
exports.getReports = async (req, res) => {
  const user = req.user;
  const { lab_order_id } = req.query;
  try {
    let where = '';
    const params = [];
    if (user.role === 'laboratory') {
      const { ownerId } = await resolveLabOwner(user.id);
      if (!ownerId) return res.status(403).json({ error: 'Lab access denied' });
      where = 'WHERE lo.laboratory_id = ?';
      params.push(ownerId);
    }
    else if (user.role === 'doctor') { where = 'WHERE lo.doctor_id = ?'; params.push(user.id); }
    else if (user.role === 'patient') { where = 'WHERE lo.patient_id = ?'; params.push(user.id); }

    if (lab_order_id) {
      where += where ? ' AND lr.lab_order_id = ?' : 'WHERE lr.lab_order_id = ?';
      params.push(lab_order_id);
    }

    const [reports] = await pool.query(
      `SELECT lr.*, lo.patient_id, lo.doctor_id, lo.laboratory_id,
              p.name as patient_name, d.name as doctor_name,
              lt.test_name, lp.lab_name
       FROM lab_reports lr
       JOIN lab_orders lo ON lr.lab_order_id = lo.id
       JOIN users p ON lo.patient_id = p.id
       JOIN users d ON lo.doctor_id = d.id
       LEFT JOIN lab_tests lt ON lr.test_id = lt.id
       LEFT JOIN laboratory_profiles lp ON lo.laboratory_id = lp.user_id
       ${where}
      ORDER BY lr.created_at DESC`,
      params
    );
    if (reports.length) {
      const ids = reports.map((r) => r.id);
      const [rows] = await pool.query(
        `SELECT lab_report_id, test_id, result_name, result_value, result_unit, normal_range, result_flag
         FROM lab_report_results
         WHERE lab_report_id IN (?)
         ORDER BY id ASC`,
        [ids]
      );
      const grouped = rows.reduce((acc, r) => {
        acc[r.lab_report_id] = acc[r.lab_report_id] || [];
        acc[r.lab_report_id].push(r);
        return acc;
      }, {});
      reports.forEach((r) => {
        r.summary_results = grouped[r.id] || [];
      });
    }
    res.json({ reports });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

// Create lab staff account
exports.createStaff = async (req, res) => {
  const { name, phone, email, password, org_role_id, department_ids } = req.body;
  try {
    const { ownerId, isOwner } = await resolveLabOwner(req.user.id);
    if (!ownerId || !isOwner) return res.status(403).json({ error: 'Only lab owner can add staff' });
    if (!name) return res.status(400).json({ error: 'Name is required' });
    if (!phone) return res.status(400).json({ error: 'Phone number is required' });
    if (!org_role_id) return res.status(400).json({ error: 'Select a role for staff' });

    const conditions = [];
    const params = [];
    if (phone) { conditions.push('u.phone = ?'); params.push(phone); }
    if (email) { conditions.push('u.email = ?'); params.push(email); }

    let userId;
    let tempPassword = null;
    if (conditions.length) {
      const [existing] = await pool.query(
        `SELECT u.id, r.name as role
         FROM users u JOIN roles r ON u.role_id = r.id
         WHERE ${conditions.join(' OR ')}
         LIMIT 1`,
        params
      );
      if (existing.length) {
        if (existing[0].role !== 'laboratory') {
          return res.status(400).json({ error: 'User exists with a different role' });
        }
        userId = existing[0].id;
      }
    }

    if (!userId) {
      const bcryptPass = password || Math.random().toString(36).slice(-8);
      if (!password) tempPassword = bcryptPass;
      const hash = await bcrypt.hash(bcryptPass, 12);
      const [result] = await pool.query(
        'INSERT INTO users (name, email, phone, password_hash, role_id, is_verified) VALUES (?, ?, ?, ?, (SELECT id FROM roles WHERE name = ?), ?)',
        [name, email || null, phone || null, hash, 'laboratory', 1]
      );
      userId = result.insertId;
    }

    const [roleRows] = await pool.query(
      'SELECT id FROM org_roles WHERE id = ? AND org_type = ? AND org_id = ?',
      [org_role_id, 'laboratory', ownerId]
    );
    if (!roleRows.length) return res.status(400).json({ error: 'Invalid role selected' });

    await pool.query('INSERT IGNORE INTO org_user_roles (user_id, org_role_id) VALUES (?, ?)', [userId, org_role_id]);

    if (Array.isArray(department_ids) && department_ids.length) {
      const [deps] = await pool.query(
        'SELECT id FROM lab_departments WHERE lab_id = ? AND id IN (?)',
        [ownerId, department_ids]
      );
      const validIds = deps.map((d) => d.id);
      if (validIds.length) {
        const rows = validIds.map((deptId) => [userId, ownerId, deptId]);
        await pool.query(
          'INSERT IGNORE INTO lab_staff_departments (staff_user_id, lab_id, department_id) VALUES ?',
          [rows]
        );
      }
    }

    res.status(201).json({ message: 'Staff account created', user_id: userId, temp_password: tempPassword });
  } catch (err) { res.status(500).json({ error: err.message }); }
};
