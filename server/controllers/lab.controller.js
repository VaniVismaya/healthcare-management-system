// Setup lab profile
exports.setupProfile = async (req, res) => {
  const { lab_name, registration_number, address, city, state, pincode, latitude, longitude, phone, email, working_hours_start, working_hours_end, working_days } = req.body;
  const userId = req.user.id;
  try {
    const { isOwner } = await resolveLabOwner(userId);
    if (!isOwner) return res.status(403).json({ error: 'Only lab owner can update profile' });
    if (!registration_number) return res.status(400).json({ error: 'Registration number is required' });
    const [[user]] = await pool.query('SELECT phone, email FROM users WHERE id = ?', [userId]);
    const contactPhone = (user?.phone || phone || '').trim() || null;
    const contactEmail = (user?.email || email || '').trim() || null;
    if (!contactPhone) return res.status(400).json({ error: 'Phone number is required' });
    const certPath = normalizePath(req.files?.certificate?.[0]?.path) || null;
    const [existing] = await pool.query('SELECT id, lab_name, registration_number, certificate_path, is_verified FROM laboratory_profiles WHERE user_id = ?', [userId]);
    const [[existingUser]] = await pool.query('SELECT is_verified FROM users WHERE id = ?', [userId]);
    if (!certPath && (!existing.length || !existing[0]?.certificate_path)) {
      return res.status(400).json({ error: 'Lab certificate is required' });
    }
    const wasVerified = !!(existingUser?.is_verified && existing[0]?.is_verified);
    const mandatoryChanged = existing.length
      && (
        String(existing[0].lab_name || '') !== String(lab_name || '') ||
        String(existing[0].registration_number || '') !== String(registration_number || '') ||
        !!certPath
      );
    
    if (existing.length) {
      const existingCert = existing[0].certificate_path || null;
      await pool.query(
        `UPDATE laboratory_profiles SET lab_name=?, registration_number=?, certificate_path=?, address=?, city=?, state=?, pincode=?, latitude=?, longitude=?, phone=?, email=?, working_hours_start=?, working_hours_end=?, working_days=?, is_verified=? WHERE user_id=?`,
        [lab_name, registration_number, certPath || existingCert, address, city, state, pincode, latitude, longitude, contactPhone, contactEmail, working_hours_start, working_hours_end, working_days, (wasVerified && mandatoryChanged) ? 0 : existing[0].is_verified, userId]
      );
      if (wasVerified && mandatoryChanged) {
        await pool.query('UPDATE users SET is_verified = 0 WHERE id = ?', [userId]);
      }
    } else {
      await pool.query(
        `INSERT INTO laboratory_profiles (user_id, lab_name, registration_number, certificate_path, address, city, state, pincode, latitude, longitude, phone, email, working_hours_start, working_hours_end, working_days)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [userId, lab_name, registration_number, certPath, address, city, state, pincode, latitude, longitude, contactPhone, contactEmail, working_hours_start, working_hours_end, working_days]
      );
    }
    res.json({ message: 'Lab profile updated' });
  } catch (err) { res.status(500).json({ error: err.message }); }
};

