const buildStandardReportPdf = async ({ filePath, meta }) => {
  const ext = path.extname(filePath || '').toLowerCase();
  const inputBytes = fs.readFileSync(filePath);
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const fontBold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

  const page = pdfDoc.addPage([595.28, 841.89]); // A4
  const { width, height } = page.getSize();
  let y = height - 48;

  page.drawText('Laboratory Report', { x: 48, y, size: 20, font: fontBold, color: rgb(0.08, 0.16, 0.22) });
  y -= 24;

  const line = (label, value) => {
    if (!value) return;
    page.drawText(`${label}:`, { x: 48, y, size: 11, font: fontBold, color: rgb(0.15, 0.15, 0.15) });
    page.drawText(String(value), { x: 160, y, size: 11, font, color: rgb(0.15, 0.15, 0.15) });
    y -= 16;
  };

  line('Patient', meta.patient_name);
  line('Patient Phone', meta.patient_phone);
  line('Doctor', meta.doctor_name);
  line('Lab', meta.lab_name);
  line('Report Title', meta.report_title || meta.test_name);
  line('Order ID', meta.lab_order_id);
  line('Report Date', meta.report_date);

  if (meta.remarks) {
    y -= 6;
    page.drawText('Remarks:', { x: 48, y, size: 11, font: fontBold, color: rgb(0.15, 0.15, 0.15) });
    y -= 14;
    const lines = String(meta.remarks).split(/\r?\n/);
    lines.forEach((ln) => {
      if (y < 60) return;
      page.drawText(ln, { x: 48, y, size: 10, font, color: rgb(0.2, 0.2, 0.2) });
      y -= 13;
    });
  }

  page.drawText('Generated by MediCare Pro', { x: 48, y: 28, size: 9, font, color: rgb(0.5, 0.5, 0.5) });

  if (ext === '.pdf') {
    const inputDoc = await PDFDocument.load(inputBytes);
    const pages = await pdfDoc.copyPages(inputDoc, inputDoc.getPageIndices());
    pages.forEach((p) => pdfDoc.addPage(p));
  } else if (ext === '.png' || ext === '.jpg' || ext === '.jpeg') {
    const img = ext === '.png' ? await pdfDoc.embedPng(inputBytes) : await pdfDoc.embedJpg(inputBytes);
    const imgPage = pdfDoc.addPage([595.28, 841.89]);
    const maxW = 595.28 - 80;
    const maxH = 841.89 - 120;
    const scale = Math.min(maxW / img.width, maxH / img.height, 1);
    const w = img.width * scale;
    const h = img.height * scale;
    const x = (595.28 - w) / 2;
    const yPos = (841.89 - h) / 2;
    imgPage.drawImage(img, { x, y: yPos, width: w, height: h });
  }

  const outputName = `${path.basename(filePath, ext)}-standard.pdf`;
  const outputPath = path.join(path.dirname(filePath), outputName);
  fs.writeFileSync(outputPath, await pdfDoc.save());
  return outputPath;
};

