// Update lab order test completion (lab)
exports.updateOrderTests = async (req, res) => {
  const { id } = req.params;
  const { tests } = req.body;

  try {
    const { ownerId, isOwner } = await resolveLabOwner(req.user.id);
    if (!ownerId) return res.status(403).json({ error: 'Lab access denied' });
    const [order] = await pool.query('SELECT laboratory_id FROM lab_orders WHERE id = ?', [id]);
    if (!order.length) return res.status(404).json({ error: 'Order not found' });
    if (order[0].laboratory_id !== ownerId) return res.status(403).json({ error: 'Not your lab order' });

    if (!Array.isArray(tests)) return res.status(400).json({ error: 'tests must be an array' });

    const testIds = [...new Set(tests.filter((t) => t && t.test_id).map((t) => t.test_id))];
    if (!isOwner && testIds.length) {
      const allowedDeptIds = await getLabStaffDepartments(req.user.id, ownerId);
      if (!allowedDeptIds.length) return res.status(403).json({ error: 'No department assigned for staff' });
      const [allowedTests] = await pool.query(
        'SELECT id FROM lab_tests WHERE id IN (?) AND lab_department_id IN (?)',
        [testIds, allowedDeptIds]
      );
      const allowedIds = new Set(allowedTests.map((t) => t.id));
      const invalid = testIds.find((tid) => !allowedIds.has(tid));
      if (invalid) return res.status(403).json({ error: 'Test not in your department' });
    }

    for (const t of tests) {
      if (!t || !t.test_id) continue;
      await pool.query(
        'UPDATE lab_order_tests SET is_completed = ? WHERE lab_order_id = ? AND test_id = ?',
        [t.is_completed ? 1 : 0, id, t.test_id]
      );
    }

    res.json({ message: 'Lab tests updated' });
  } catch (err) { res.status(500).json({ error: err.message }); }
};

// Upload report
exports.uploadReport = async (req, res) => {
  const { lab_order_id, test_id, report_title, remarks, result_value, result_unit } = req.body;
  const filePath = req.file?.path;

  if (!filePath) return res.status(400).json({ error: 'Report file required' });

  try {
    const { ownerId, isOwner } = await resolveLabOwner(req.user.id);
    if (!ownerId) return res.status(403).json({ error: 'Lab access denied' });
    const [orderInfo] = await pool.query(
      `SELECT lo.id as lab_order_id, lo.patient_id, lo.doctor_id, lo.laboratory_id,
              p.name as patient_name, p.phone as patient_phone,
              d.name as doctor_name,
              lp.lab_name
       FROM lab_orders lo
       JOIN users p ON lo.patient_id = p.id
       JOIN users d ON lo.doctor_id = d.id
       LEFT JOIN laboratory_profiles lp ON lo.laboratory_id = lp.user_id
       WHERE lo.id = ? AND lo.laboratory_id = ?`,
      [lab_order_id, ownerId]
    );

    const order = orderInfo[0];
    if (!order) return res.status(404).json({ error: 'Lab order not found' });

    let summaryResults = req.body.summary_results || req.body.results || [];
    if (typeof summaryResults === 'string') {
      try { summaryResults = JSON.parse(summaryResults); } catch { summaryResults = []; }
    }
    if (!Array.isArray(summaryResults)) summaryResults = [];

    if (!summaryResults.length && (result_value || result_unit || test_id)) {
      summaryResults = [{
        test_id: test_id || null,
        result_name: null,
        result_value: result_value || null,
        result_unit: result_unit || null,
        normal_range: null
      }];
    }

    summaryResults = summaryResults.slice(0, 5);
    const testIds = [...new Set(summaryResults.map((r) => r?.test_id).filter(Boolean))];

    if (!isOwner && testIds.length) {
      const allowedDeptIds = await getLabStaffDepartments(req.user.id, ownerId);
      if (!allowedDeptIds.length) return res.status(403).json({ error: 'No department assigned for staff' });
      const [allowedTests] = await pool.query(
        'SELECT id FROM lab_tests WHERE id IN (?) AND lab_department_id IN (?)',
        [testIds, allowedDeptIds]
      );
      const allowedIds = new Set(allowedTests.map((t) => t.id));
      const invalid = testIds.find((tid) => !allowedIds.has(tid));
      if (invalid) return res.status(403).json({ error: 'Test not in your department' });
    }

    let testMeta = {};
    if (testIds.length) {
      const [tests] = await pool.query(
        'SELECT id, test_name, normal_range FROM lab_tests WHERE id IN (?)',
        [testIds]
      );
      testMeta = tests.reduce((acc, t) => {
        acc[t.id] = t;
        return acc;
      }, {});
    }

    const normalizedResults = summaryResults.map((r) => {
      const tid = r?.test_id ? Number(r.test_id) : null;
      const meta = tid ? testMeta[tid] : null;
      const name = r?.result_name || meta?.test_name || r?.name || 'Result';
      const normalRange = r?.normal_range || meta?.normal_range || null;
      const value = r?.result_value ?? r?.value ?? null;
      const unit = r?.result_unit ?? r?.unit ?? null;
      const flag = computeResultFlag(value, normalRange);
      return {
        test_id: tid,
        result_name: name,
        result_value: value,
        result_unit: unit,
        normal_range: normalRange,
        result_flag: flag
      };
    }).filter((r) => r.result_name);

    let primary = normalizedResults[0] || null;
    let reportPath = filePath;
    try {
      const generated = await buildStandardReportPdf({
        filePath,
        meta: {
          lab_order_id: order.lab_order_id,
          report_title,
          test_name: primary?.result_name || null,
          remarks,
          patient_name: order.patient_name,
          patient_phone: order.patient_phone,
          doctor_name: order.doctor_name,
          lab_name: order.lab_name,
          report_date: new Date().toISOString().slice(0, 10)
        }
      });
      if (generated) reportPath = generated;
    } catch {
      reportPath = filePath;
    }

    const storedPath = normalizePath(reportPath);

    const [result] = await pool.query(
      `INSERT INTO lab_reports
       (lab_order_id, test_id, report_title, report_file_path, remarks, uploaded_by, result_value, result_unit, result_flag, normal_range_snapshot)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
      [
        lab_order_id,
        primary?.test_id || test_id || null,
        report_title,
        storedPath,
        remarks,
        req.user.id,
        primary?.result_value || result_value || null,
        primary?.result_unit || result_unit || null,
        primary?.result_flag || computeResultFlag(result_value, primary?.normal_range || null),
        primary?.normal_range || null
      ]
    );

    const reportId = result.insertId;
    if (normalizedResults.length) {
      const rows = normalizedResults.map((r) => ([
        reportId,
        r.test_id || null,
        r.result_name,
        r.result_value || null,
        r.result_unit || null,
        r.normal_range || null,
        r.result_flag || null
      ]));
      await pool.query(
        'INSERT INTO lab_report_results (lab_report_id, test_id, result_name, result_value, result_unit, normal_range, result_flag) VALUES ?',
        [rows]
      );
    }

    const io = req.app.get('io');
    await sendNotificationWithEmit(io, order.patient_id, 'Lab Report Uploaded',
      'Your lab report is ready. You can view it in the app.', 'lab_order', lab_order_id, 'lab_order');
    await sendNotificationWithEmit(io, order.doctor_id, 'Lab Report Uploaded',
      'Lab report for your patient is available.', 'lab_order', lab_order_id, 'lab_order');

    res.status(201).json({ report_id: reportId, message: 'Report uploaded successfully' });
  } catch (err) { res.status(500).json({ error: err.message }); }
};

